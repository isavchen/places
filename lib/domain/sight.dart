  int id;
  String name;
  double lat, lon;
  String url;
  String details;
  String type;
  List<String> galery;

  Sight({
    required this.id,
    required this.name,
    required this.lat,
    required this.lon,
    required this.url,
    required this.details,
    required this.type,
    required this.galery,
  });
}
